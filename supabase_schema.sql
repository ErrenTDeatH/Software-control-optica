-- 1. Tabla: usuarios
CREATE TABLE IF NOT EXISTS public.usuarios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username TEXT UNIQUE NOT NULL,
    password TEXT NOT NULL,
    role TEXT NOT NULL DEFAULT 'Optometrista',
    nombre TEXT NOT NULL,
    cargo TEXT,
    registro TEXT,
    telefono TEXT,
    sucursal TEXT DEFAULT 'Matriz',
    accesos JSONB DEFAULT '["Inicio", "Pacientes", "Generar Orden", "Trabajos", "Ventas", "Inventario", "Contabilidad", "Citas", "Asistente IA", "Usuarios", "Configuracion"]'::jsonb,
    creado_el TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Tabla: sucursales
CREATE TABLE IF NOT EXISTS public.sucursales (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre TEXT UNIQUE NOT NULL,
    direccion TEXT,
    telefono TEXT,
    ciudad TEXT DEFAULT 'Quito',
    creado_el TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Tabla: pacientes
CREATE TABLE IF NOT EXISTS public.pacientes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identificacion TEXT UNIQUE,
    nombre TEXT NOT NULL,
    nombres TEXT,

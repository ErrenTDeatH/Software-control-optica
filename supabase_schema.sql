-- 10. Tabla: caja_diaria
CREATE TABLE IF NOT EXISTS public.caja_diaria (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha DATE DEFAULT CURRENT_DATE,
    monto_apertura NUMERIC(10,2) DEFAULT 0.00,
    monto_cierre NUMERIC(10,2) DEFAULT 0.00,
    abierta_por TEXT,
    cerrada_por TEXT,
    sucursal TEXT DEFAULT 'Matriz',
    estado TEXT DEFAULT 'Abierta',
    creado_el TIMESTAMPTZ DEFAULT NOW()
);

-- 11. Tabla: citas
CREATE TABLE IF NOT EXISTS public.citas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    paciente_id BIGINT REFERENCES public.pacientes(id) ON DELETE SET NULL,
    paciente_nombre TEXT,
    fecha_cita DATE NOT NULL,
    hora_cita TIME NOT NULL,
    optometrista TEXT,
    motivo TEXT,
    estado TEXT DEFAULT 'Confirmada',
    sucursal TEXT DEFAULT 'Matriz',
    creado_el TIMESTAMPTZ DEFAULT NOW()
);

-- 12. Tabla: auditoria
CREATE TABLE IF NOT EXISTS public.auditoria (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha_hora TIMESTAMPTZ DEFAULT NOW(),
    usuario TEXT,
    nombre_usuario TEXT,
    accion TEXT,
    entidad TEXT,
    detalle TEXT,
    sucursal TEXT DEFAULT 'Matriz'
